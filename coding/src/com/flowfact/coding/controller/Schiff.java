package com.flowfact.coding.controller;

public class Schiff {
	
	private int mSchiffSize;
	private int x;
	private int y;

	public int getmSchiffSize() {
		return mSchiffSize;
	}


	public Schiff(int mSchiffSize, int x, int y) {
		this.mSchiffSize = mSchiffSize;
		this.x = x;
		this.y = y;
		
	}


	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}


	
	

}
